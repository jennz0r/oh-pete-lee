var htmlFunction, html;

$(document).ready(function() {
  switch(location.pathname) {
    case "/":
      htmlFunction = JST['handlebars/index.hbs'];
      html = htmlFunction(dataIndex);
      break;
    case "/music/":
      htmlFunction = JST['handlebars/videography.hbs'];
      html = htmlFunction(dataMusic);
      break;
    case "/branded/":
      htmlFunction = JST['handlebars/videography.hbs'];
      html = htmlFunction(dataBranded);
      break;
    case "/narrative/":
      htmlFunction = JST['handlebars/videography.hbs'];
      html = htmlFunction(dataNarrative);
      break;
    case "/photography/works/":
      htmlFunction = JST['handlebars/photography_works.hbs'];
      html = htmlFunction(dataWorksPhotography);
      break;
    case "/photography/food/":
      htmlFunction = JST['handlebars/photography_food.hbs'];
      html = htmlFunction(dataFoodPhotography);
      break;
    case "/contact/":
      htmlFunction = JST['handlebars/contact.hbs'];
      html = htmlFunction(dataContact);
      break;
    case "/dontbeahero/":
      htmlFunction = JST['handlebars/dontbeahero.hbs'];
      html = htmlFunction(dataHero);
      break;
  };

  // Prepend the template to the body
  $('body').prepend(html);

  // Use fit vids to make videos responsive
  $(".modal__video").fitVids();

  if (location.pathname == '/contact/') {
    $('body').css('background-image', 'url(/img/pete01.jpg)');
  }
  else if (location.pathname === '/photography/works/' || location.pathname === '/photography/food/') {
    $('body').css('background-color', '#000');
  }
  else {
    $('body').css('background-color', '#222');
  }

  $(document).foundation();
});




